CREATE TABLE BI_CONTRATOS AS 

select c.id,c.data_inicio,c.data_fim,numero_contrato,c.valor_contrato,pc.parcela,pc.valor,pc.juros,pc.multa,pc.correcao,pc.desconto,pc.valor_pago,pc.data_vencimento,pc.data_pagamento,
tgf.descricao tp_grupo_financeiro,gf.receita_despesa,gf.descricao grupo_fianceiro,

case when gf.receita_despesa = 0 then pc.valor_pago end receita,
case when gf.receita_despesa = 1 then pc.valor_pago end despesa,
case when gf.receita_despesa = 0 then 'RECEITA' else 'DESPESA' end receita_despesa_desc,
case when c.situacao = 1 then 'ATIVO' when c.situacao = 2 then 'INATIVO' when c.situacao = 3 then 'PARALISADO'  when c.situacao = 4 then 'SUSPENSO' else 'QUITADO' end situacao_desc ,

(select sum(valor_pago) 
from contratos sub_sel_c,
     parcelas_contratos sub_sel_pc,
     tipos_grupos_financeiros sub_sel_tgf,
     grupos_financeiros sub_sel_gf
where sub_sel_pc.contrato_id                = sub_sel_c.id and
      sub_sel_c.tipo_grupo_financeiro_id    = sub_sel_tgf.id and
      sub_sel_tgf.grupo_financeiro_id       = sub_sel_gf.id and
      sub_sel_c.id                          = c.id 
group by sub_sel_tgf.id ) soma_valor_pago 

from contratos c,
     parcelas_contratos pc,
     tipos_grupos_financeiros tgf,
     grupos_financeiros gf
where pc.contrato_id = c.id and
      c.tipo_grupo_financeiro_id = tgf.id and
      tgf.grupo_financeiro_id = gf.id
;

